// Connect to MetaMask
async function connect() {
  if (window.ethereum) {
    try {
      await window.ethereum.enable();
      console.log('Connected to MetaMask');
      initApp();
    } catch (error) {
      console.error(error);
    }
  } else {
    console.error('MetaMask not found');
  }
}

// Initialize the app
function initApp() {
    const web3 = new Web3(window.ethereum);
    const contract = new web3.eth.Contract(contractABI, contractAddress);
    
    displayStakedAmount(contract);
  
    // Listen for changes in the staked amount and update the display
    contract.events.Staked({ filter: { staker: accounts[0] } })
      .on('data', () => {
        displayStakedAmount(contract);
      });
  }
  

// Display the current staked amount for the connected account
async function displayStakedAmount(contract) {
  const accounts = await web3.eth.getAccounts();
  const stakedAmount = await contract.methods.getStakedAmount(accounts[0]).call();
  console.log('Staked Amount:', stakedAmount);

  // Update the UI with the staked amount
  const stakedAmountElement = document.getElementById('stakedAmount');
  stakedAmountElement.textContent = `Staked Amount: ${stakedAmount}`;
}

// Stake tokens
async function stake() {
    const stakeAmount = document.getElementById('stakeAmount').value;
    if (!stakeAmount) {
      console.error('Please enter the amount to stake');
      return;
    }
  
    const web3 = new Web3(window.ethereum);
    const contract = new web3.eth.Contract(contractABI, contractAddress);
  
    try {
      const accounts = await web3.eth.getAccounts();
      await contract.methods.stake(stakeAmount).send({ from: accounts[0] });
      console.log('Tokens staked successfully');
      displayStakedAmount(contract);
    } catch (error) {
      console.error(error);
    }
  }

// Withdraw staked tokens
async function withdraw() {
  const withdrawAmount = document.getElementById('withdrawAmount').value;
  if (!withdrawAmount) {
    console.error('Please enter the amount to withdraw');
    return;
  }

  const web3 = new Web3(window.ethereum);
  const contract = new web3.eth.Contract(contractABI, contractAddress);

  try {
    const accounts = await web3.eth.getAccounts();
    await contract.methods.withdraw(withdrawAmount).send({ from: accounts[0] });
    console.log('Tokens withdrawn successfully');
    displayStakedAmount(contract);
  } catch (error) {
    console.error(error);
  }
}
